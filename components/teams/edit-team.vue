<template>
  <modal v-if="show" @close="hideModal">
    <div slot="header">
      <ul>
        <li>
          <div class="row-wrapper">
            <h3 class="title">{{ $t("edit_team") }}</h3>
            <div>
              <button class="icon" @click="hideModal">
                <i class="material-icons">close</i>
              </button>
            </div>
          </div>
        </li>
      </ul>
    </div>
    <div slot="body">
      <ul>
        <li>
          <input
            type="text"
            v-model="name"
            :placeholder="editingTeam.name"
            @keyup.enter="saveTeam"
          />
        </li>
      </ul>
      <ul>
        <li>
          <div class="row-wrapper">
            <label for="memberList">{{ $t("team_member_list") }}</label>
            <div>
              <button class="icon" @click="clearContent($event)" v-tooltip.bottom="$t('clear')">
                <i class="material-icons">clear_all</i>
              </button>
            </div>
          </div>
        </li>
      </ul>
      <ul v-for="(member, index) in members" :key="index">
        <li>
          <input
            :placeholder="$t('email')"
            :name="'param' + index"
            v-model="member.key"
            autofocus
          />
        </li>
        <li>
          <span class="select-wrapper">
            <v-popover>
              <input
                :placeholder="$t('permissions')"
                :name="'value' + index"
                :value="
                  typeof member.value === 'string' ? member.value : JSON.stringify(member.value)
                "
                readonly
              />
              <template slot="popover">
                <div>
                  <button class="icon" v-close-popover @click="member.value = 'OWNER'">
                    OWNER
                  </button>
                </div>
                <div>
                  <button class="icon" v-close-popover @click="member.value = 'EDITOR'">
                    EDITOR
                  </button>
                </div>
                <div>
                  <button class="icon" v-close-popover @click="member.value = 'VIEWER'">
                    VIEWER
                  </button>
                </div>
              </template>
            </v-popover>
          </span>
        </li>
        <div>
          <li>
            <button
              class="icon"
              @click="removeTeamMember(index)"
              v-tooltip.bottom="$t('delete')"
              id="member"
            >
              <i class="material-icons">delete</i>
            </button>
          </li>
        </div>
      </ul>
      <ul>
        <li>
          <button class="icon" @click="addTeamMember">
            <i class="material-icons">add</i>
            <span>{{ $t("add_new") }}</span>
          </button>
        </li>
      </ul>
    </div>
    <div slot="footer">
      <div class="row-wrapper">
        <span></span>
        <span>
          <button class="icon" @click="hideModal">
            {{ $t("cancel") }}
          </button>
          <button class="icon primary" @click="saveTeam">
            {{ $t("save") }}
          </button>
        </span>
      </div>
    </div>
  </modal>
</template>

<script>
import team_utils from "~/helpers/teams/utils"

export default {
  props: {
    show: Boolean,
    editingTeam: Object,
    editingteamID: String,
  },
  data() {
    return {
      rename: null,
      doneButton: '<i class="material-icons">done</i>',
      members: [],
    }
  },
  watch: {
    editingTeam: function (update) {
      console.log("editingTeam", update)
    },
  },
  // mounted: {
  //   printValues() {
  //     console.log(this.$props.teamMembers)
  //   },
  // },
  computed: {
    editingTeamCopy() {
      return this.editingTeam
    },
    memberString() {
      console.log("memberString")
    },
    name: {
      get() {
        return this.editingTeam.name
      },
      set(name) {
        this.rename = name
      },
    },
    teamMembers: {
      get() {
        // this.updatedMembers = []
        const n = this.editingTeam.members.length
        for (let i = 0; i < n; i++) {
          const item = {
            key: this.editingTeam.members[i].user.email,
            value: this.editingTeam.members[i].role,
            // readOnly: true,
          }
          this.members.push(item)
        }
        return this.members
      },
      set(updatedMembers) {
        this.members = updatedMembers
      },
    },
  },
  methods: {
    clearContent(e) {
      console.log("clearContent")
      e.target.innerHTML = this.doneButton
      this.$toast.info(this.$t("cleared"), {
        icon: "clear_all",
      })
      setTimeout(() => (e.target.innerHTML = '<i class="material-icons">clear_all</i>'), 1000)
    },
    addTeamMember() {
      console.log(this.teamMembers)
      let value = { key: "", value: "" }
      this.members.push(value)
      console.log("addTeamMember")
    },
    removeTeamMember(index) {
      this.members.splice(index, 1)
      console.log("removeTeamMember")
    },
    validateEmail(emailID) {
      if (/^[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]+@[a-zA-Z0-9-]+(?:\.[a-zA-Z0-9-]+)*$/.test(emailID)) {
        return true
      }
      return false
    },
    saveTeam() {
      if (this.$data.rename !== null && this.$data.rename.replace(/\s/g, "").length < 6) {
        this.$toast.error(this.$t("string_length_insufficient"), {
          icon: "error",
        })
        console.log("String length less than 6")
        return
      }
      console.log("saveTeam", this.members)
      this.$data.members.forEach((element) => {
        if (!this.validateEmail(element.key)) {
          this.$toast.error(this.$t("invalid_emailID_format"), {
            icon: "error",
          })
          console.log("Email id format invalid")
          return
        }
      })
      this.$data.members.forEach((element) => {
        // Call to the graphql mutation
        team_utils
          .addTeamMemberByEmail(this.$apollo, element.value, element.key, this.editingteamID)
          .then((data) => {
            // Result
            this.$toast.success(this.$t("team_saved"), {
              icon: "done",
            })
            this.hideModal()
          })
          .catch((error) => {
            // Error
            this.$toast.error(this.$t("error_occurred"), {
              icon: "done",
            })
            console.error(error)
          })
      })
      if (this.$data.rename !== null) {
        const newName = this.name === this.$data.rename ? this.name : this.$data.rename
        if (!/\S/.test(newName))
          return this.$toast.error(this.$t("team_name_empty"), {
            icon: "error",
          })
        // Call to the graphql mutation
        if (this.name !== this.rename)
          team_utils
            .renameTeam(this.$apollo, newName, this.editingteamID)
            .then((data) => {
              // Result
              this.$toast.success(this.$t("team_saved"), {
                icon: "done",
              })
              this.hideModal()
            })
            .catch((error) => {
              // Error
              this.$toast.error(this.$t("error_occurred"), {
                icon: "done",
              })
              console.error(error)
            })
      }
      this.hideModal()
    },
    hideModal() {
      this.$emit("hide-modal")
      this.$data.name = undefined
    },
  },
}
</script>
